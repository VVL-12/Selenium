package week1.day2;

public class LearnForLoop {
	
	public static void main(String[] args)
	{ 
		for (int i=2;i<5;i++) {
			System.out.println(i);
		}
		System.out.println("-----");
		
		for (int i=5;i>1;i--) {
			System.out.println(i);
		}
		
		for (int i=1;i<=5;i++) {
			System.out.println("----");
			System.out.println(i);
		}
		for (int n=10;n>=1;n--) {
			System.out.println(n);
			
		}
			
	}
	

}