package week1.day2;

public class LearnElseIf {
	
	public static void main(String[] args) {
		int a=7;
		int b=7;
		
		if(a>b) {
			System.out.println("a is greater than b");
		}
		
		else if(a<b) {
			System.out.println("b is greater that a");}
		
		else {
			System.out.println("a is equal to b");
		}
		}
		
}
